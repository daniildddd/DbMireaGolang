export const msToSeconds = (ms: number) => ms / 1000;
export const msToMinutes = (ms: number) => ms / (60 * 1000);
export const msToHours = (ms: number) => ms / (60 * 60 * 1000);

export const secondsToMs = (s: number) => s * 1000;
export const minutesToMs = (min: number) => min * 60 * 1000;
export const hoursToMs = (h: number) => h * 60 * 60 * 1000;
